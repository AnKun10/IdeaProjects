//Bài 2: Tính chu vi và diện tích hình tròn khi biết bán kính của nó.
public class BTVN2 {
    public static void main(String[] args) {
        float radius = 21.36f;

        System.out.println("Hình tròn có bán kính là "+radius);
        System.out.println("Chu vi hình tròn đó là "+(2*radius)+"π");
        System.out.println("Diện tích hình tròn đó là "+(radius*radius)+"π");
    }
}
